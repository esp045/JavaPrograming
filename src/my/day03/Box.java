package my.day03;
import java.util.*;

public class Box {

	public static void main(String[] args) {
			double w;
			double h;
			double area;
			double peri;
			
			Scanner input = new Scanner(System.in);
			
			System.out.print("사각형의 가로를 입력하시오: ");
			w = input.nextDouble();
			
			System.out.print("사각형의 세로를 입력하시오: ");
			h = input.nextDouble();
			
			area = w * h;
			peri = 2*w + 2*h;
			
			System.out.println("사각형의 넓이는 " +area);
			System.out.println("사각형의 둘레는 " +peri);
			

	}
}
